def sum(a, b):
    """Retourne la somme de a et b.

    Args:
        a (float): Le premier nombre à additionner.
        b (float): Le deuxième nombre à additionner.

    Returns:
        float: Le résultat de a + b.
    """
    return a + b


def subtraction(a, b):
    """Retourne la soustraction de a et b.

    Args:
        a (float): Un premier nombre.
        b (float): Un deuxième nombre.

    Returns:
        float: Le résultat de a - b.
    """
    return a - b
